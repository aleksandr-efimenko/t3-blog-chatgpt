import { type NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import { signIn, signOut, useSession } from "next-auth/react";
import { api } from "~/utils/api";
import { useCallback, useState } from "react";
import Button from "~/components/Button";
import { Router } from "next/router";
import PostElement from "~/components/PostElement";
import SimpleMDE, { SimpleMdeReact } from "react-simplemde-editor";
import "easymde/dist/easymde.min.css";

const CreatePost: NextPage = () => {
  const [prompt, setPrompt] = useState("");
  const [content, setContent] = useState("");
  const [openAIFetchingStatus, setOpenAIFetchingStatus] = useState<
    "idle" | "pending" | "fulfilled" | "rejected"
  >("idle");
  const response = api.openAi.generateCompletion.useMutation({
    onMutate: (variables) => {
      console.log("pending");
      setOpenAIFetchingStatus("pending");
    },
    onError: (error) => {
      console.log(error);
      setOpenAIFetchingStatus("rejected");
    },
    onSuccess: (data) => {
      console.log("success");
      if (!data.response) return;
      const text = data.response?.choices?.[0]?.text || "No response";
      console.log(data.response);
      setContent(text);
      setOpenAIFetchingStatus("fulfilled");
    },
  });

  const createPost = api.posts.createPost.useMutation();

  const generatePost = async (e: React.FormEvent<HTMLFormElement>) => {
    e.preventDefault();
    if (openAIFetchingStatus === "pending") return;
    setContent("");
    await response.mutateAsync({ text: prompt });
  };

  const savePost = () => {
    if (!prompt || !content) return;
    createPost.mutate({
      title: prompt,
      content: content,
      published: true,
    });
    setPrompt("");
    setContent("");
  };

  const onChangeMDE = useCallback((value: string) => {
    setContent(value);
  }, []);

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex flex-col items-center justify-center">
        <div className="container flex flex-col items-center justify-center gap-12 px-4 py-16 ">
          <h1 className="text-2xl tracking-tight text-white">Create post</h1>
          <div className="flex w-full gap-10">
            <form onSubmit={generatePost} className="flex flex-1 flex-col">
              <input
                className="my-2 block w-full rounded-md border border-slate-300 bg-white py-2 pl-2 text-2xl shadow-sm placeholder:italic placeholder:text-slate-400 focus:border-sky-500 focus:outline-none focus:ring-1 focus:ring-sky-500 sm:text-sm"
                placeholder="Add prompt"
                type="text"
                onChange={(e) => setPrompt(e.target.value)}
                value={prompt}
              />

              <Button
                type="submit"
                loading={openAIFetchingStatus === "pending"}
                disabled={!prompt || openAIFetchingStatus === "pending"}
              >
                {openAIFetchingStatus === "pending"
                  ? "In progress"
                  : "Generate"}
              </Button>
              {/* <textarea
                className="my-2 block w-full rounded-md border border-slate-300 bg-white py-2 pl-2 text-2xl shadow-sm placeholder:italic placeholder:text-slate-400 focus:border-sky-500 focus:outline-none focus:ring-1 focus:ring-sky-500 sm:text-sm"
                cols={50}
                onChange={(e) => setContent(e.target.value)}
                placeholder="Content"
                rows={20}
                value={content}
              /> */}

              <div className="bg-slate-300 mt-3">
                <SimpleMdeReact value={content} onChange={onChangeMDE} />
              </div>
            </form>
            <div className="w-full flex-1">
              <PostElement post={{ title: prompt, content: content }} />
              <Button onClick={savePost}>Post</Button>
            </div>
          </div>
        </div>
      </main>
    </>
  );
};

export default CreatePost;
